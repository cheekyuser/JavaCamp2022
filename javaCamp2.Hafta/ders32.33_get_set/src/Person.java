public class Person {
    private String _name;
    private String _hairColor;
    private String _eyeColor;
    private int _height;
    private int _weight;


    public String get_name() {
        return _name;
    }

    public void set_name(String name) {
        _name = name;
    }

    public String get_hairColor() {
        return _hairColor;
    }

    public void set_hairColor(String hairColor) {
        _hairColor = hairColor;
    }

    public String get_eyeColor() {
        return _eyeColor;
    }

    public void set_eyeColor(String eyeColor) {
        _eyeColor = eyeColor;
    }

    public int get_height() {
        return _height;
    }

    public void set_height(int height) {
        _height = height;
    }

    public int get_weight() {
        return _weight;
    }

    public void set_weight(int weight) {
        _weight = weight;
    }
}
