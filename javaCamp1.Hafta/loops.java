public class loops {
    public static void main(String[] args) {
        for (int i=1; i<=5; i++){
            System.out.println(i);
        }
        System.out.println("For loop is over");

        int sayac = 1;
        while (sayac<=10){
            System.out.println(sayac);
            sayac++;
        }System.out.println("While loop is over");
    }
}