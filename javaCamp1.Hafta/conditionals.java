public class conditionals {
    public static void main(String[] args) {
        int num = 10;
        if (num > 12){
            System.out.println("number is bigger than 12");
        }else if (num < 12){
            System.out.println("Number is smaller than 12");
        }else{
            System.out.println("number is equal to 12");

        }
    }
}